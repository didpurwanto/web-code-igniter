					當今的清真生活發展正大力呈現出社會對於清真產品和服務的需求，同樣地，也關乎在印尼建置起清真教法醫院及服務，這為醫療行業和其他相關產業提供了機會、挑戰及巨大潛力。

					為了推廣該地區的伊斯蘭教法醫院，印尼伊斯蘭保健醫療機構網絡（MUKISI）籌辦了第一屆IHEX（國際伊斯蘭醫療保健會議暨博覽會），該博覽會內容包含論壇、研討會、展覽，以及所有與清真醫療相關範疇。這是第一次我們將實質參與者、學者、研究人員、企業主和所有相關各方聚集在一起，就印尼的清真醫療進行布達、討論及介紹清真產品和服務。

					MUI(印尼伊斯蘭宗教學者理事會)主席馬恩．阿明（Dr. KH Ma'ruf Amin）及MUI 印尼駐台灣代表處-心忠管理顧問公司Sincung Halal for Taiwan林心瀅執行長皆受邀出席致詞。

