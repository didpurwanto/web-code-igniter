					The development of halal lifestyle today proves to be a strong indicator for the society's need for halal products and services. Likewise in healthcare services with the establishment of shariah hospital in Indonesia. It provides opportunities, challenges and great potential to healthcare sector and other related industries. In order to promote shariah hospital in the region, Islamic Health Institution Network of Indonesia (MUKISI) is organizing the 1st IHEX (International Islamic Healthcare Conference and Expo) which is packaged into seminar, workshops and exhibition of all related elements that support the implementation of shariah hospitals. This very first occasion will bring together all parties, from practitioners, academics, researchers, business owners and all related stakeholders whose aims are to disseminate, discuss and introduce halal products and services in halal healthcare in Indonesia.
