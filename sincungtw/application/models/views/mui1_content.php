         	<div class="alert alert-success" align="left">
            <h3 class="margin-bottom">關於Majelis Ulama Indonesia, MUI </h3>
            
                 <p><h4>
                 	MUI係於1975年由蘇哈托總統指示成立，但因印尼採政教分離制度，基於尊重各種宗教，故不列為政府編制內機構。MUI現任最高長老(教宗)為Dr. KH Ma'ruf Amin，已來臺多次，對臺印象良好且相當親臺。
                 </p>
                 <br />

                 <p><h4>
        					MUI成立目的是為了統合各地伊斯蘭組織對於古蘭經及聖訓的釋令(Fatwa)，提供穆斯林作為日常生活的規範及準則，並提升穆斯林生活福祉，因此在當地被視為地位最高且最權威的伊斯蘭組織，全球人數最多的伊斯蘭教士聯合會（Nahdlatul Ulama, NU）及印尼第2大伊斯蘭團體穆罕默迪亞協會（Muhammadiyah）等均是旗下組織。
        				</p>
        				<br />

        				<p><h4>
        					由於MUI負責對穆斯林提供釋令，自然成為了當地清真認證(Halal Certificate)的制定及核發單位，並設有LPPOM MUI(食品、藥物及化妝品管理署)及Fatwa Commission(FC, 教法會議)兩大單位，其中LPPOM MUI負責辦理審查及稽核等清真認證相關業務，而Fatwa Commission則負責針對前者審查結果判斷是否為清真(Halal)，通過FC後，再由LPPOM MUI核發清真認證，此外LPPOM MUI署長Dr. Lukmanul Hakim, M. Si亦為世界清真食品理事會(WHFC)主席。
        				</p>
        				<br />

        				<p><h4>
        					隨著全球貿易日益發達，當愈來愈多的國外產品進入印尼市場時，當地穆斯林開始愈來愈擔心進口產品是否為清真，因此對於商品具備清真標章的要求日益增加，並促使MUI加速推廣清真標章，鼓勵業者為自家產品取得清真認證，進而使穆斯林在採購商品時可以更加安心。許多國際品牌，如雀巢、OREO、百事可樂、雪碧、美國A&W速食連鎖店、日本Pepper Lunch 胡椒廚房、丸龜製麵、明治食品、Asahi的軟性飲料及韓國Mujigae餐館等均已取得MUI清真標章。

                 </p>
          
