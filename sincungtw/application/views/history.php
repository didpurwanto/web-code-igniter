        <h6 class="heading">Profil</h6>
        <p>Nahdlatul Ulama (NU) menganut paham Ahlussunah Wal Jama’ah, sebuah pola pikir yang mengambil jalan tengah antara ekstrim aqli (rasionalis) dengan kaum ekstrim naqli (skripturalis). Karena itu sumber pemikiran bagi NU tidak hanya Al-Qur’an dan Sunnah, tetapi juga menggunakan kemampuan akal ditambah dengan realitas empirik. <br /><br />


		Salah satu ciri khas NU adalah dengan adanya program-program yang bersifat kolosal, yaitu mengerahkan dan melibatkan ribuan hingga jutaan orang dalam satu masa yang sama seperti acara istighosah kubro (doa bersama), pengajian (ceramah agama), yasin dan tahlil, thariqat naqsabandiyah dan qadiriyah, semaan khataman al-qur’an dan lain sebagainya.<br /><br />

		Berangkat dari fenomena kultural inilah, telah menggerakkan hati dan langkah kaum nadliyin yang berada di Taiwan untuk melahirkan pengurus cabang istimewa Nahdlatul Ulama (PCI-NU) di Taiwan sebagai wadah ekspresi sekaligus simbol identitas diri.<br />

		Pijakan ini bukan tidak beralasan mengingat Taiwan adalah potensi besar bagi lahan gerakan kultural NU. Hal tersebut cukup realistis jika dengan potensi komunitas pelajar, mahasiswa, dan pekerja Indonesia yang telah menjejakkan kakinya di Taiwan yang diperkirakan mencapai puluhan ribu orang. Tentu potensi besar ini tak dapat diabaikan begitu saja, baik secara sosial maupun kultural.<br /><br />

		Berbeda dengan NU yang ada di Indonesia, kinerja PCI-NU Taiwan lebih fleksibel menyesuaikan dengan ketentuan undang-undang yang berlaku di Taiwan. Pemerintah Taiwan mempunyai undang-undang yang mengatur, mengontrol dan mengawasi aktivitas organisasi-organisasi sipil ataupun individu-individu yang berada di Taiwan. Sehingga PCI-NU Taiwan hanya menentukan dan melaksanakan program-program yang sekiranya tidak mengganggu ‘kestabilan’ keamanan dan di bawah koridor yang ditetapkan pemerintah Taiwan dan Indonesia.</p><br />
