					<h2>清真認證機構名單 </h2>
					<h4>List Of Halal Certification Bodies </h4> <br />
					<ol>
						<li>
						<b>LPPOM MUI 承認經批准的清真認證機構簽發的清真證書，但只用于在該清真認證機構所在國家生產的產品。但在歐洲生產的產品除外, 可使用由任何位於歐洲的認證機構批准的清真食品使用清真證書。</b> <br />
						LPPOM MUI recognize halal certificates issued by approved halal certification body only for product produced in the country where the halal certification body located, except for product produced in Europe can be used halal certificate by any approved halal certification body located in Europe.</li>
						<li>
						<b>LPPOM MUI保留要求其他證明文件的可能性, 以澄清某些已經認證產品的關鍵點。</b> <br />
						There are still possibilities for LPPOM MUI to ask supporting document to clarify the critical points of certain certified products.</li>
						<li>
						<b>MUI的法令對已經批准的外國清真認證機構名單的有效期為 2 (兩年),每年將進行一次觀察和評估</b> <br />
						The MUI decree regarding list of approved foreign halal certification body is effective for 2 (two) years as of the date it is stipulated and it will be monitored and evaluated once a year.</li>
						<li>
						<b>LPPOM MUI總共批准來自25個國家的42個清真認證機構, 包括32個被批准 (牛) 屠宰類, 38 個機構被批准原料類別, 以及17 個機構被批准為風味類別。</b> <br />
						There are 42 Halal Certifier Bodies approved by LPPOM MUI from 25 country, contain 32 bodies approved for (cattle) slaughtering category, 38 bodies approved for raw material category, 17 bodies approved for flavor category. </li>
						<li>
						<b>2018年1月更新。由MUI批准的清真認證機構名單可以在這裡下載</b> <br />
						Updated January 2018. The List Of Halal Certification Bodies Approve by MUI can download in <a target="_blank" href="http://www.halalmui.org/images/stories/pdf/LSH/LSHLN-LPPOM%20MUI.pdf"><b>here</b></a></li>
						</li>
					</ol>
